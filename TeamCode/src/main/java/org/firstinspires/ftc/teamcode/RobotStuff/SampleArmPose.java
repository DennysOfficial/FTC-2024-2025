package org.firstinspires.ftc.teamcode.RobotStuff;

public class SampleArmPose {
    public double wristLittleTwistAlignmentAngle;
    public double wristBigTwistAlignmentPosition;
    public double liftPosition;
    public double pivotPosition;

    public SampleArmPose(double wristLittleTwistAlignmentAngle, double wristBigTwistAlignmentPosition, double liftPosition, double pivotPosition) {
        this.wristLittleTwistAlignmentAngle = wristLittleTwistAlignmentAngle;
        this.wristBigTwistAlignmentPosition = wristBigTwistAlignmentPosition;
        this.liftPosition = liftPosition;
        this.pivotPosition = pivotPosition;
    }
}
